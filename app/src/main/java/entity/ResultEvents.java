
package entity;

import java.io.Serializable;
import java.util.List;
import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class ResultEvents implements Serializable {

    @SerializedName("Status")
    @Expose
    private Status status;
    @SerializedName("Result_event")
    @Expose
    private List<ResultEvent> resultEvent = null;
    @SerializedName("Statistics")
    @Expose
    private Statistics statistics;
    @SerializedName("ParsDID")
    @Expose
    private ParsDID parsDID;

    public Status getStatus() {
        return status;
    }

    public void setStatus(Status status) {
        this.status = status;
    }

    public List<ResultEvent> getResultEvent() {
        return resultEvent;
    }

    public void setResultEvent(List<ResultEvent> resultEvent) {
        this.resultEvent = resultEvent;
    }

    public Statistics getStatistics() {
        return statistics;
    }

    public void setStatistics(Statistics statistics) {
        this.statistics = statistics;
    }

    public ParsDID getParsDID() {
        return parsDID;
    }

    public void setParsDID(ParsDID parsDID) {
        this.parsDID = parsDID;
    }

}
